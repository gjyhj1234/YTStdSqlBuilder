    private static void EmitCreateTableSqlString(StringBuilder sb, EntityModel model)
    {
        sb.AppendLine("            var createTableSql =");
        sb.Append($"                \"CREATE TABLE IF NOT EXISTS \\\"{model.TableName}\\\" (\" +");
        sb.AppendLine();

        for (int i = 0; i < model.Columns.Count; i++)
        {
            var col = model.Columns[i];
            var suffix = "";
            if (col.IsPrimaryKey)
                suffix = " PRIMARY KEY";
            else if (col.IsRequired || !col.IsNullable)
                suffix = " NOT NULL";

            var sep = i < model.Columns.Count - 1 ? ", " : "";
            sb.AppendLine($"                \"\\\"{col.ColumnName}\\\" {col.PgType}{suffix}{sep}\" +");
        }

        if (model.IsTenantTable)
            sb.AppendLine("                \") PARTITION BY LIST (tenant_id);\";");
        else
            sb.AppendLine("                \");\";");
    }

