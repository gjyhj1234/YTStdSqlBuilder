    private static void EmitEnsureColumnLength(StringBuilder sb, EntityModel model)
    {
        var tableName = model.TableName;
        var className = model.ClassName;

        sb.AppendLine($"        /// <summary>检查并扩展 {className} 表字段长度</summary>");
        sb.AppendLine("        public static async ValueTask<bool> EnsureColumnLength(int tenantId, long userId)");
        sb.AppendLine("        {");

        if (!string.IsNullOrEmpty(model.ViewSql))
        {
            sb.AppendLine($"            Logger.Info(tenantId, userId, \"[{className}DAL.EnsureColumnLength] 视图不支持字段扩展\");");
            sb.AppendLine("            return false;");
            sb.AppendLine("        }");
            return;
        }

        sb.AppendLine($"            Logger.Info(tenantId, userId, \"[{className}DAL.EnsureColumnLength] 开始检查字段, 表名={tableName}\");");
        sb.AppendLine();
        sb.AppendLine($"            var (result, fields) = await DB.GetFieldsInfor(\"{Esc(tableName)}\", reader => new DbField");
        sb.AppendLine("            {");
        sb.AppendLine("                FieldName = reader.GetString(0),");
        sb.AppendLine("                DataType = reader.GetString(1),");
        sb.AppendLine("                MaxLength = reader.IsDBNull(2) ? null : reader.GetInt32(2),");
        sb.AppendLine("                NumericPrecision = reader.IsDBNull(3) ? null : reader.GetInt32(3),");
        sb.AppendLine("                NumericScale = reader.IsDBNull(4) ? null : reader.GetInt32(4),");
        sb.AppendLine("                IsNullable = reader.GetString(5) == \"YES\",");
        sb.AppendLine("                IsPrimaryKey = reader.GetBoolean(6)");
        sb.AppendLine("            });");
        sb.AppendLine();
        sb.AppendLine("            if (fields == null) return false;");
        sb.AppendLine("            var changed = false;");
        sb.AppendLine();

        // Check varchar / decimal columns that have Length specified
        for (int i = 0; i < model.Columns.Count; i++)
        {
            var col = model.Columns[i];
            if (col.Length <= 0) continue;

            sb.AppendLine($"            // 检查字段: {col.ColumnName}");
            sb.AppendLine("            {");
            sb.AppendLine("                DbField? existing = null;");
            sb.AppendLine($"                for (int i = 0; i < fields.Count; i++)");
            sb.AppendLine("                {");
            sb.AppendLine($"                    if (fields[i].FieldName == \"{col.ColumnName}\")");
            sb.AppendLine("                    {");
            sb.AppendLine("                        existing = fields[i];");
            sb.AppendLine("                        break;");
            sb.AppendLine("                    }");
            sb.AppendLine("                }");
            sb.AppendLine("                if (existing != null)");
            sb.AppendLine("                {");

            if (col.ClrTypeName == "string" && col.Length > 0)
            {
                sb.AppendLine($"                    if (existing.MaxLength.HasValue && existing.MaxLength.Value < {col.Length})");
                sb.AppendLine("                    {");
                sb.AppendLine($"                        Logger.Info(tenantId, userId,");
                sb.AppendLine($"                            $\"[{className}DAL.EnsureColumnLength] 扩展字段 {col.ColumnName}: {{existing.MaxLength}} -> {col.Length}\");");
                sb.AppendLine($"                        await DB.AlterTable(\"{Esc(tableName)}\", \"{col.ColumnName}\",");
                sb.AppendLine($"                            \"varchar\", \"{col.Length}\", \"\", {(col.IsNullable ? "true" : "false")}, {(col.IsPrimaryKey ? "true" : "false")});");
                sb.AppendLine("                        changed = true;");
                sb.AppendLine("                    }");
            }
            else if (col.ClrTypeName == "decimal")
            {
                var len = col.Length > 0 ? col.Length : 12;
                sb.AppendLine($"                    if (existing.NumericPrecision.HasValue && existing.NumericPrecision.Value < {len})");
                sb.AppendLine("                    {");
                sb.AppendLine($"                        Logger.Info(tenantId, userId,");
                sb.AppendLine($"                            $\"[{className}DAL.EnsureColumnLength] 扩展字段 {col.ColumnName}: precision={{existing.NumericPrecision}} -> {len}\");");
                sb.AppendLine($"                        await DB.AlterTable(\"{Esc(tableName)}\", \"{col.ColumnName}\",");
                sb.AppendLine($"                            \"decimal\", \"{len}\", \"{col.Precision}\", {(col.IsNullable ? "true" : "false")}, {(col.IsPrimaryKey ? "true" : "false")});");
                sb.AppendLine("                        changed = true;");
                sb.AppendLine("                    }");
            }

            sb.AppendLine("                }");
            sb.AppendLine("            }");
            sb.AppendLine();
        }

        sb.AppendLine("            return changed;");
        sb.AppendLine("        }");
    }

    private static void EmitCreateIndexIfNotExists(StringBuilder sb, EntityModel model)
    {
        var tableName = model.TableName;
        var className = model.ClassName;

        sb.AppendLine($"        /// <summary>创建 {className} 表索引</summary>");
        sb.AppendLine("        public static async ValueTask<bool> CreateIndexIfNotExists(int tenantId, long userId)");
        sb.AppendLine("        {");

        if (!string.IsNullOrEmpty(model.ViewSql))
        {
            sb.AppendLine($"            Logger.Info(tenantId, userId, \"[{className}DAL.CreateIndexIfNotExists] 视图不支持索引\");");
            sb.AppendLine("            return false;");
            sb.AppendLine("        }");
            return;
        }

        sb.AppendLine($"            Logger.Info(tenantId, userId, \"[{className}DAL.CreateIndexIfNotExists] 开始创建索引, 表名={tableName}\");");
        sb.AppendLine("            var created = false;");
        sb.AppendLine();

        for (int i = 0; i < model.Indexes.Count; i++)
        {
            var idx = model.Indexes[i];
            var unique = idx.IsUnique ? "true" : "false";
            var columns = string.Join(", ", idx.Columns);

            sb.AppendLine($"            // 索引: {idx.IndexName}");
            sb.AppendLine($"            var r{i} = await DB.CreateIndex(\"{Esc(idx.IndexName)}\", \"{Esc(tableName)}\", \"{Esc(columns)}\", {unique});");
            sb.AppendLine($"            if (r{i} == DDLStatus.Success)");
            sb.AppendLine("            {");
            sb.AppendLine($"                Logger.Info(tenantId, userId, \"[{className}DAL.CreateIndexIfNotExists] 索引创建成功: {idx.IndexName}\");");
            sb.AppendLine("                created = true;");
            sb.AppendLine("            }");
            sb.AppendLine($"            else if (r{i} == DDLStatus.Existed)");
            sb.AppendLine("            {");
            sb.AppendLine($"                Logger.Info(tenantId, userId, \"[{className}DAL.CreateIndexIfNotExists] 索引已存在, 跳过: {idx.IndexName}\");");
            sb.AppendLine("            }");
            sb.AppendLine();
        }

        sb.AppendLine("            return created;");
        sb.AppendLine("        }");
    }

    private static string Esc(string s)
    {
        return s.Replace("\\", "\\\\").Replace("\"", "\\\"");
    }
