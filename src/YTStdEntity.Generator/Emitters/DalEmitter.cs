        // Audit table DDL
        sb.AppendLine($"{indent}var createAuditTableSql =");
        sb.AppendLine($"{indent}    \"CREATE TABLE IF NOT EXISTS \\\"{auditTable}\\\" (\" +");
        sb.AppendLine($"{indent}    \"audit_id BIGSERIAL PRIMARY KEY, \" +");
        sb.AppendLine($"{indent}    \"id BIGINT NOT NULL, \" +");
        sb.AppendLine($"{indent}    \"opt CHAR(1) NOT NULL, \" +");
        sb.AppendLine($"{indent}    \"operated_at TIMESTAMPTZ NOT NULL DEFAULT now(), \" +");
        sb.AppendLine($"{indent}    \"operator_id BIGINT NOT NULL, \" +");
        if (model.IsTenantTable)
        {
            sb.AppendLine($"{indent}    \"tenant_id INT NOT NULL, \" +");
        }
        sb.AppendLine($"{indent}    \"snapshot JSONB NOT NULL\" +");
        if (model.IsTenantTable)
            sb.AppendLine($"{indent}    \") PARTITION BY LIST (tenant_id);\";");
        else
            sb.AppendLine($"{indent}    \");\";");
        sb.AppendLine();
        sb.AppendLine($"{indent}Logger.Info(tenantId, userId, $\"[{className}DAL.CreateTableIfNotExists] 审计表SQL: {{createAuditTableSql}}\");");
