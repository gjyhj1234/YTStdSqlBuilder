    private static void EmitCreateTableIfNotExists(StringBuilder sb, EntityModel model)
    {
        var tableName = model.TableName;
        var auditTableName = tableName + "_audit";
        var className = model.ClassName;

        sb.AppendLine($"        /// <summary>创建 {className} 表及相关对象（审计表、触发器等）</summary>");
        sb.AppendLine("        public static async ValueTask<bool> CreateTableIfNotExists(int tenantId, long userId, bool createLogTable)");
        sb.AppendLine("        {");
        sb.AppendLine($"            Logger.Info(tenantId, userId, \"[{className}DAL.CreateTableIfNotExists] 开始创建, 表名={tableName}\");");
        sb.AppendLine();

        // Check if table exists
        sb.AppendLine($"            var existsResult = await DB.GetTableInfor(\"{Esc(tableName)}\");");
        sb.AppendLine("            if (existsResult.Success && existsResult.RowsAffected > 0)");
        sb.AppendLine("            {");
        sb.AppendLine($"                Logger.Info(tenantId, userId, \"[{className}DAL.CreateTableIfNotExists] 表已存在, 跳过: {tableName}\");");
        sb.AppendLine("                return false;");
        sb.AppendLine("            }");
        sb.AppendLine();

        // Build CREATE TABLE SQL
        sb.AppendLine("            // === 主表 ===");
        EmitCreateTableSqlString(sb, model);
        sb.AppendLine();
        sb.AppendLine($"            Logger.Info(tenantId, userId, $\"[{className}DAL.CreateTableIfNotExists] 建表SQL: {{createTableSql}}\");");
        sb.AppendLine($"            await DB.CreateTable(\"{Esc(tableName)}\", createTableSql);");
        sb.AppendLine($"            Logger.Info(tenantId, userId, \"[{className}DAL.CreateTableIfNotExists] 主表创建成功: {tableName}\");");
        sb.AppendLine();

